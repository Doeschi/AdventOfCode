class Robot {
    constructor(x, y, xVel, yVel) {
        this.x = x;
        this.y = y;
        this.xVel = xVel;
        this.yVel = yVel;
    }
}

robots = [];
fieldWidth = 101;
fieldHeight = 103;

cellWidth = 0
cellHeight = 0;

moving = false;
secondsPassed = 0;
startSecond = 7687;

function setup() {
    createCanvas(700, 700);
    frameRate(30);

    cellWidth = width / fieldWidth;
    cellHeight = height / fieldHeight;

    const parsedArray = JSON.parse(input);
    robots = parsedArray.map(item => new Robot(parseInt(item.x), parseInt(item.y), parseInt(item.xVel), parseInt(item.yVel)));
    move(startSecond);
}

function draw() {
    background(220);
    noStroke();

    fill(120);
    robots.forEach(robot => {
        rect(robot.x * cellWidth, robot.y * cellHeight, cellWidth, cellHeight);
    });

    fill(0);
    textSize(20);
    text("Seconds: " + secondsPassed, 50, height - 30);

    if (moving) {
        move(1);
    } else if (keyIsPressed === true) {
        if (key === 'a') {
            this.move(1);
        } else if (key === 'd') {
            this.move(-1);
        }
    }
}
function keyPressed() {
    if (key === 'e') {
        this.move(1);
    } else if (key === 'q') {
        this.move(-1);
    } else if (key === 's') {
        moving = !moving;
    }
}

function move(seconds) {
    robots.forEach(robot => {
        robot.x = (robot.x + robot.xVel * seconds) % fieldWidth;
        if (robot.x < 0)
            robot.x += fieldWidth;

        robot.y = (robot.y + robot.yVel * seconds) % fieldHeight;
        if (robot.y < 0)
            robot.y += fieldHeight;
    });

    secondsPassed += seconds;
}

function getSafetyFactor() {
    var safetyFactor = 1;

    var yOffset = 0;

    for (let y = 0; y < 2; ++y) {
        var xOffset = 0;
        for (let x = 0; x < 2; ++x) {

            let counter = 0;

            robots.forEach(robot => {
                if (robot.x >= xOffset &&
                    robot.x < xOffset + Math.floor(fieldWidth / 2) &&
                    robot.y >= yOffset &&
                    robot.y < yOffset + Math.floor(fieldHeight / 2)) {
                    ++counter;
                }
            });

            safetyFactor *= counter;
            xOffset += Math.floor(fieldWidth / 2) + 1;
        }

        yOffset += Math.floor(fieldHeight / 2) + 1;
    }

    return safetyFactor;
}

